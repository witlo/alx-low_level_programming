#include "main.h"

/**
* _strcpy - Copies a string from the source array to the destination array
*
* @dest: Array where the string is to be copied to
* @src: Array where the string is to be copied from
*
* Return: A pointer to the destination array
*/

char *_strcpy(char *dest, char *src)
{
	int i = 0;

	while (src[i] != '\0')
	{
		*(dest + i) = src[i];
		i++;
	}

	*(dest + i) = '\0';

	return (dest);
}
