#include "main.h"

/**
* _strchr - Searches for a character within a string
*
* @s: The string to be searched
*
* @c: The character to look for
*
* Return: Pointer to the first occurrence of @c in @s, or NULL if not found
*
*/

char *_strchr(char *s, char c)
{
	while (*s)
	{
		s++;

		if (*s == c)
		{
			return (s);
		}
	}

	return (0);
}
